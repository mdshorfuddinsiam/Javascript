// double 
function doubleNumber(num){
    console.log('---------');
    console.log(num);
    let result = num * 2;
    console.log(result);
}
doubleNumber(7);
doubleNumber(10);
doubleNumber(25);
let price = 50;
doubleNumber(price);
let age = 26;
doubleNumber(age);

// Difference
function difference(num1, num2){
    console.log('---------');
    console.log(num1, num2);
    let diff = num1 - num2;
    console.log(diff);
}
difference(35, 26);
difference(50, 20);
let num1 = 20;
let num2 = 30;
difference(num1, num2);